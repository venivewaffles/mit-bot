# –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞
start_bot() {
    log "ü§ñ –ó–∞–ø—É—Å–∫–∞–µ–º Telegram –±–æ—Ç–∞..."
    
    source venv/bin/activate
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ –∑–∞–ø—É—â–µ–Ω –ª–∏ —É–∂–µ –±–æ—Ç
